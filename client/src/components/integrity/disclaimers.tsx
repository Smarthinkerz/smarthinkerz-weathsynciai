import { ReactNode } from "react";
import { Info, Scale, Sparkles, ShieldAlert, Database } from "lucide-react";
import { useTranslation } from "react-i18next";
import { cn } from "@/lib/utils";
import {
  Tooltip,
  TooltipContent,
  TooltipProvider,
  TooltipTrigger,
} from "@/components/ui/tooltip";

interface DisclaimerProps {
  className?: string;
  children?: ReactNode;
}

/**
 * FinancialDisclaimer — persistent "informational only / not financial advice"
 * notice. Render this ABOVE any financial/portfolio/investment guidance so the
 * user sees it before results.
 */
export function FinancialDisclaimer({ className, children }: DisclaimerProps) {
  const { t } = useTranslation();
  return (
    <div
      role="note"
      data-testid="disclaimer-financial"
      className={cn(
        "flex items-start gap-2.5 rounded-md border border-amber-300/70 bg-amber-50 px-3 py-2.5 text-amber-900",
        className,
      )}
    >
      <Info className="mt-0.5 h-4 w-4 shrink-0 text-amber-600" aria-hidden />
      <p className="text-xs leading-relaxed">
        <span className="font-semibold">
          {t("disclaimer.financialTitle", "Informational only — not financial advice.")}
        </span>{" "}
        {children ??
          t(
            "disclaimer.financialBody",
            "WealthSync provides AI-generated insights for research and education. It is not a registered investment adviser. Verify figures independently and consult a qualified professional before making financial decisions.",
          )}
      </p>
    </div>
  );
}

