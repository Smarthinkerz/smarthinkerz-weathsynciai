/**
 * AIEstimateBadge — provenance label for a number that is produced by the AI
 * model rather than computed from a verified data source. Be honest: an LLM
 * estimate is not a calculated metric.
 */
export function AIEstimateBadge({
  className,
  label,
  tooltip,
}: {
  className?: string;
  label?: string;
  tooltip?: string;
}) {
  const { t } = useTranslation();
  return (
    <TooltipProvider delayDuration={150}>
      <Tooltip>
        <TooltipTrigger asChild>
          <span
            data-testid="badge-ai-estimate"
            className={cn(
              "inline-flex cursor-help items-center gap-1 rounded-full border border-violet-200 bg-violet-50 px-1.5 py-0.5 text-[10px] font-medium leading-none text-violet-700",
              className,
            )}
          >
            <Sparkles className="h-2.5 w-2.5" aria-hidden />
            {label ?? t("provenance.aiEstimate", "AI estimate")}
          </span>
        </TooltipTrigger>
        <TooltipContent className="max-w-[220px] text-xs">
          {tooltip ??
            t(
              "provenance.aiEstimateTooltip",
              "Generated by the AI model from your inputs and context — not a figure calculated from verified market data. Treat as directional, not precise.",
            )}
        </TooltipContent>
      </Tooltip>
    </TooltipProvider>
  );
}

